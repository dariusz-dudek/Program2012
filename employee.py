class Employee:

    def __init__(self, first_name, last_name, sex, department_number, salary, age, children, marital_status):
        self.first_name = first_name
        self.last_name = last_name
        self.sex = sex
        self.department_number = department_number
        self.salary = salary
        self.age = age
        self.children = children
        self.marital_status = marital_status
